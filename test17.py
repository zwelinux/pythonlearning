# list []
mybasket = ['apple', 'orange', 'banana', 'peach', 'rum', 'wisky']
print(type(mybasket))
# tuple ()
mybasket = ('apple', 'orange', 'banana', 'peach', 'rum', 'wisky')
print(type(mybasket))
# set {}
mybasket = {'apple', 'orange', 'banana', 'peach', 'rum', 'wisky'}
print(type(mybasket))
